#include <stdio.h>

int main(void)
{
    int n = 0;
    scanf("%d", &n); // 1234
    // 1234 % 10 = 4;
    // 1234 / 10 = 123;

    // 123 % 10 = 3;
    // 123 / 10 = 12;

    // 12 % 10 = 2;
    // 12 / 10 = 1;

    // 1 % 10 = 1;
    // 1 / 10 = 0;
    while (n)
    {
        printf("%d", n % 10);
        n /= 10;
    }

    return 0;
}